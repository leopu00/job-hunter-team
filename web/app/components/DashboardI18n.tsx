// ── Context ───────────────────────────────────────────────────────────────────

type DashboardI18nCtx = {
  locale: DashboardLocale
  t: (key: string) => string
}

const DashboardI18nContext = createContext<DashboardI18nCtx>({
  locale: 'en',
  t: (key) => key,
})

export function useDashboardT() {
  return useContext(DashboardI18nContext)
}

// ── Provider ──────────────────────────────────────────────────────────────────

export function DashboardI18nProvider({ children }: { children: ReactNode }) {
  const [locale, setLocale] = useState<DashboardLocale>('en')

  const fetchLocale = useCallback(async () => {
    const res = await fetch('/api/i18n?t=' + Date.now()).catch(() => null)
    if (!res?.ok) return
    const data = await res.json()
    if (data.current && ['it', 'en', 'hu'].includes(data.current)) {
      setLocale(data.current as DashboardLocale)
    }
  }, [])

  useEffect(() => { fetchLocale() }, [fetchLocale])

  const t = useCallback((key: string): string => {
    return TRANSLATIONS[locale]?.[key] ?? TRANSLATIONS['it']?.[key] ?? key
  }, [locale])

  return (
    <DashboardI18nContext.Provider value={{ locale, t }}>
      {children}
    </DashboardI18nContext.Provider>
  )
}
